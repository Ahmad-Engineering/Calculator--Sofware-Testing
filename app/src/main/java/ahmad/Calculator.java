package ahmad;

public class Calculator {
    
    public static int adding (int num1, int num2) {
        return num1 + num2;
    }

    public static float multiplicationFunction (float num1, float num2) {
        return num1 * num2;
    }

    public static float divisionFunction (float num1, float num2) {
        return num1 / num2;
    }

    public static float minusFunction (float num1, float num2) {
        return num1 - num2;
    }

    public static float modulationFunction (float num1, float num2) {
        return num1 % num2;
    }
}
